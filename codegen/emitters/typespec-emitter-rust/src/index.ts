function emitClientPackage(program: Program, repoRoot: string): void {
  const manifest: string[] = [BANNER, '// Source: specs/typespec/backend/main.tsp.', ''];
  manifest.push('// Endpoint manifest. Hand-written tooling iterates this list when');
  manifest.push('// it needs to enumerate the backend HTTP surface (CLI generation,');
  manifest.push('// drift checks). Method wrappers live alongside in wrapper_methods.rs.');
  manifest.push('');
  appendEndpointManifest(program, manifest, 'OpenboxBackend', 'BACKEND_ENDPOINT_MANIFEST');
  writeGeneratedFile(repoRoot, 'rust/src/client/generated', 'endpoint_manifest.rs', manifest);

  emitWrapperMethods(program, repoRoot, {
    namespaceName: 'OpenboxBackend',
    outDir: 'rust/src/client/generated',
    fileName: 'wrapper_methods.rs',
    source: 'specs/typespec/backend/main.tsp',
    clientStruct: 'OpenBoxClient',
    clientModule: 'crate::client',
  });

  writeGeneratedModRs(repoRoot, 'rust/src/client/generated', [
    'endpoint_manifest',
    'wrapper_methods',
  ]);
  writeGeneratedReadme(repoRoot, 'rust/src/client/generated', [
    { source: 'specs/typespec/backend/main.tsp', reproduces: 'endpoint_manifest.rs: the BACKEND_ENDPOINT_MANIFEST array enumerating every backend HTTP operation' },
    { source: 'specs/typespec/backend/main.tsp', reproduces: 'wrapper_methods.rs: one async fn on OpenBoxClient per backend HTTP operation' },
  ]);
}

