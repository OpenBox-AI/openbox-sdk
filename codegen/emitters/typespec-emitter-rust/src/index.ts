function writeGeneratedFile(repoRoot: string, dirRel: string, name: string, lines: string[]): void {
  const out = resolvePathFs(repoRoot, dirRel, name);
  mkdirSync(dirname(out), { recursive: true });
  writeFileSync(out, lines.join('\n').replace(/\n+$/, '\n'));
}

function writeGeneratedModRs(repoRoot: string, dirRel: string, modules: string[]): void {
  const lines = [BANNER, ''];
  for (const m of modules) lines.push(`mod ${m};`);
  lines.push('');
  // Re-export every module's items except `wrapper_methods`. That file
  // contains inherent `impl` blocks (no public types or fns), so a
  // `pub use wrapper_methods::*` is empty and trips the compiler's
  // unused-imports lint.
  for (const m of modules) {
    if (m === 'wrapper_methods') continue;
    lines.push(`pub use ${m}::*;`);
  }
  lines.push('');
  writeGeneratedFile(repoRoot, dirRel, 'mod.rs', lines);
}

interface ReadmeRow {
  source: string;
  reproduces: string;
}

function writeGeneratedReadme(repoRoot: string, dirRel: string, rows: ReadmeRow[]): void {
  const lines: string[] = [];
  lines.push(`# \`${dirRel.replace(/^rust\//, '')}\``);
  lines.push('');
  lines.push('**AUTO-GENERATED. Do not hand-edit.**');
  lines.push('');
  lines.push('| Source | Reproduces |');
  lines.push('|---|---|');
  for (const r of rows) {
    lines.push(`| \`${r.source}\` | ${r.reproduces} |`);
  }
  lines.push('| `codegen/emitters/typespec-emitter-rust/src/index.ts` | the emit logic |');
  lines.push('');
  lines.push('Regenerate with `npm run specs:compile`.');
  lines.push('');
  const out = resolvePathFs(repoRoot, dirRel, 'README.md');
  mkdirSync(dirname(out), { recursive: true });
  writeFileSync(out, lines.join('\n'));
}

// ---------------------------------------------------------------------------
// String helpers
// ---------------------------------------------------------------------------

function pascal(s: string): string {
  // Handles snake_case, kebab-case, dotted, and existing PascalCase
  // inputs. Splits on any non-alphanumeric run, capitalizes each
  // chunk, and rejoins. Already-PascalCase strings pass through
  // because the split yields a single chunk.
  const parts = s.split(/[^a-zA-Z0-9]+/).filter(Boolean);
  if (parts.length === 0) return s;
  return parts.map((p) => p.charAt(0).toUpperCase() + p.slice(1)).join('');
}

function snake(s: string): string {
  return s
    .replace(/([a-z0-9])([A-Z])/g, '$1_$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1_$2')
    .toLowerCase();
}

function escapeRustKeyword(name: string): string {
  const reserved = new Set([
    'as', 'async', 'await', 'break', 'const', 'continue', 'crate', 'dyn', 'else',
    'enum', 'extern', 'false', 'fn', 'for', 'if', 'impl', 'in', 'let', 'loop',
    'match', 'mod', 'move', 'mut', 'pub', 'ref', 'return', 'self', 'Self',
    'static', 'struct', 'super', 'trait', 'true', 'type', 'union', 'unsafe',
    'use', 'where', 'while', 'yield',
  ]);
  return reserved.has(name) ? `r#${name}` : name;
}

function renderDoc(text: string | undefined): string[] {
  if (!text) return [];
  return text.split('\n').map((line) => `/// ${line.trimEnd()}`);
}
