// Endpoint manifest
// ---------------------------------------------------------------------------

interface ManifestEntry {
  operationId: string;
  path: string;
  verb: string;
}

function appendEndpointManifest(
  program: Program,
  file: string[],
  namespaceName: string,
  constName: string,
): void {
  const ns = findNamespace(program, namespaceName);
  const entries: ManifestEntry[] = [];
  if (ns) {
    walkOperations(ns, (operationId, path, verb) => {
      entries.push({ operationId, path, verb });
    });
  }

  file.push(`/// One entry per HTTP operation declared in the spec. Stable iteration`);
  file.push(`/// order matches declaration order.`);
  file.push('#[derive(Debug, Clone, Copy)]');
  file.push('pub struct EndpointManifestEntry {');
  file.push("    pub operation_id: &'static str,");
  file.push("    pub path: &'static str,");
  file.push("    pub verb: &'static str,");
  file.push('}');
  file.push('');
  file.push(`pub const ${constName}: &[EndpointManifestEntry] = &[`);
  for (const e of entries) {
    file.push('    EndpointManifestEntry {');
    file.push(`        operation_id: ${JSON.stringify(e.operationId)},`);
    file.push(`        path: ${JSON.stringify(e.path)},`);
    file.push(`        verb: ${JSON.stringify(e.verb.toUpperCase())},`);
    file.push('    },');
  }
  file.push('];');
}

function walkOperations(
  ns: Namespace,
  visit: (operationId: string, path: string, verb: string) => void,
): void {
  for (const [, op] of ns.operations) {
    const httpRoute = (op as unknown as { route?: { path: string } }).route;
    if (!httpRoute) continue;
    const verb = (op as unknown as { verb?: string }).verb ?? 'POST';
    visit(op.name, httpRoute.path, verb);
  }
  for (const [, child] of ns.namespaces) {
    walkOperations(child, visit);
  }
}

// ---------------------------------------------------------------------------
// Wrapper-method emit
// ---------------------------------------------------------------------------

interface WrapperEmitOptions {
  namespaceName: string;
  outDir: string;
  fileName: string;
  source: string;
  /** Name of the hand-written struct the impl block extends, e.g.
   *  `OpenBoxClient`. The struct lives in `<clientModule>` and is
   *  expected to expose `request_get` / `request_post` / etc. */
  clientStruct: string;
  clientModule: string;
}

interface WrapperMethodSpec {
  methodName: string;
  verb: 'get' | 'post' | 'put' | 'patch' | 'delete';
  path: string;
  pathParams: { name: string }[];
  bodyType: string | null;
  responseType: string;
  hasQuery: boolean;
  doc: string | undefined;
}

let CURATED_METHOD_NAMES: Record<string, string> | null = null;
function curatedMethodNames(repoRoot: string): Record<string, string> {
  if (CURATED_METHOD_NAMES) return CURATED_METHOD_NAMES;
  try {
    CURATED_METHOD_NAMES = JSON.parse(
      readFileSync(resolvePathFs(repoRoot, 'codegen/method-names.json'), 'utf-8'),
    ) as Record<string, string>;
  } catch {
    CURATED_METHOD_NAMES = {};
  }
  return CURATED_METHOD_NAMES;
}

function emitWrapperMethods(
  program: Program,
  repoRoot: string,
  opts: WrapperEmitOptions,
): void {
  const ns = findNamespace(program, opts.namespaceName);
  if (!ns) return;

  const [ops] = listHttpOperationsIn(program, ns);
  const file: string[] = [BANNER, `// Source: ${opts.source}.`, ''];
  file.push('#![allow(unused_imports, clippy::too_many_arguments)]');
  file.push('use crate::types::*;');
  file.push(`use ${opts.clientModule}::{${opts.clientStruct}, ApiError};`);
  file.push('');

  const ctx: EmitNsContext = {
    program,
    file,
    emittedModels: collectGlobalEmittedModels(program),
  };

  const names = curatedMethodNames(repoRoot);
  const seen = new Set<string>();
  const methods: WrapperMethodSpec[] = [];
  for (const op of ops) {
    const spec = methodSpecFor(ctx, op, names);
    if (!spec) continue;
    if (seen.has(spec.methodName)) continue;
    seen.add(spec.methodName);
    methods.push(spec);
  }

  file.push(`impl ${opts.clientStruct} {`);
  for (const m of methods) {
    appendWrapperMethod(file, m);
    file.push('');
  }
  // Drop the trailing blank line so the file ends with a single \n.
  while (file.length > 0 && file[file.length - 1] === '') file.pop();
  file.push('}');

  writeGeneratedFile(repoRoot, opts.outDir, opts.fileName, file);
}

function collectGlobalEmittedModels(program: Program): Set<string> {
  // Wrapper-method types reference models from any `Openbox*` namespace
  // (backend ops sometimes return env / shared types). Union every
  // top-level OpenBox namespace so renderType resolves names from any
  // of them without a per-namespace re-lookup.
  const out = new Set<string>();
  const root = program.getGlobalNamespaceType();
  for (const [name, ns] of root.namespaces) {
    if (!name.startsWith('Openbox')) continue;
    for (const m of collectEmittedModels(ns)) out.add(m);
  }
  return out;
}

type HttpOp = ReturnType<typeof listHttpOperationsIn>[0][number];

function methodSpecFor(
  ctx: EmitNsContext,
  op: HttpOp,
  curated: Record<string, string>,
): WrapperMethodSpec | null {
  const opId = op.operation.name;
  let methodName = curated[opId];
  if (!methodName) {
    // Fallback: strip the `<Tag>Controller_` prefix. Mirrors the TS
    // emitter heuristic; new ops without a curated entry get the bare
    // verb (e.g. `list`) and may collide. Curating in
    // codegen/method-names.json fixes it.
    methodName = opId;
    const m = opId.match(/^([A-Z][a-zA-Z]*Controller)_(.+)$/);
    if (m) methodName = m[2];
  }

  const verb = op.verb.toLowerCase();
  if (!isHttpVerb(verb)) return null;

  const pathParams: { name: string }[] = [];
  let hasQuery = false;
  for (const p of op.parameters.parameters) {
    if (p.type === 'path') pathParams.push({ name: p.name });
    else if (p.type === 'query') hasQuery = true;
  }

  let bodyType: string | null = null;
  if (op.parameters.body && 'type' in op.parameters.body && op.parameters.body.type) {
    bodyType = renderTypeInner(ctx, op.parameters.body.type);
  }

  // Pick the first 2xx body. `Body<T>` wrappers expose `.type` as the
  // unwrapped inner T already (TypeSpec instantiates the template).
  let responseType = 'serde_json::Value';
  for (const r of op.responses) {
    const status =
      typeof r.statusCodes === 'number'
        ? r.statusCodes
        : typeof r.statusCodes === 'object' && r.statusCodes !== null && 'start' in r.statusCodes
          ? (r.statusCodes as { start: number }).start
          : 0;
    if (status < 200 || status >= 300) continue;
    for (const content of r.responses) {
      if (content.body && 'type' in content.body && content.body.type) {
        responseType = renderTypeInner(ctx, content.body.type);
        break;
      }
    }
    break;
  }

  return {
    methodName,
    verb,
    path: op.path,
    pathParams,
    bodyType,
    responseType,
    hasQuery,
    doc: getDoc(ctx.program, op.operation as unknown as Operation),
  };
}

function isHttpVerb(v: string): v is WrapperMethodSpec['verb'] {
  return v === 'get' || v === 'post' || v === 'put' || v === 'patch' || v === 'delete';
}

function appendWrapperMethod(file: string[], m: WrapperMethodSpec): void {
  for (const line of renderDoc(m.doc)) file.push(`    ${line}`);

  const args: string[] = ['&self'];
  for (const p of m.pathParams) args.push(`${snake(p.name)}: &str`);
  if (m.bodyType) args.push(`body: &${m.bodyType}`);
  if (m.hasQuery) args.push('query: Option<&serde_json::Value>');

  file.push(
    `    pub async fn ${snake(m.methodName)}(${args.join(', ')}) -> Result<${m.responseType}, ApiError> {`,
  );

  // `format!("/agent/{}/approval/{}/decide", agent_id, approval_id)`
  // for any path with `{param}` placeholders, otherwise a static &str.
  let pathExpr: string;
  if (m.pathParams.length === 0) {
    pathExpr = JSON.stringify(m.path);
  } else {
    const fmt = m.path.replace(/\{[^}]+\}/g, '{}');
    const callArgs = m.pathParams.map((p) => snake(p.name)).join(', ');
    pathExpr = `&format!(${JSON.stringify(fmt)}, ${callArgs})`;
  }

  // Every `request_<verb>` helper takes the same shape:
  //   self.request_<verb>(path, body, query).await
  // Body and query slots accept `Option<&T>`. Empty slots emit
  // `None::<&()>` (body) or `None::<&serde_json::Value>` (query); the
  // sentinel types resolve Rust generic inference and impl `Serialize`
  // without allocating. A uniform call shape covers irregular cases
  // like GET-with-body and DELETE-with-body the spec carries.
  const bodyArg = m.bodyType ? 'Some(body)' : 'None::<&()>';
  const queryArg = m.hasQuery ? 'query' : 'None::<&serde_json::Value>';
  file.push(`        self.request_${m.verb}(${pathExpr}, ${bodyArg}, ${queryArg}).await`);
  file.push('    }');
}

