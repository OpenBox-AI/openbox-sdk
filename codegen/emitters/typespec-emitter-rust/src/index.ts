// TypeSpec → Rust emitter for the OpenBox SDK monorepo.
//
// Mirrors codegen/emitters/typespec-emitter-typescript/ shape. Every
// type, enum, and constant in the Rust crate's package modules is
// produced here from the TypeSpec source of truth at
// `specs/typespec/`. Hand-written Rust code is limited to:
//
//   - `rust/Cargo.toml`      package metadata and platform-glue deps.
//   - `rust/src/lib.rs`      top-level `pub mod` declarations.
//   - `rust/src/<pkg>/mod.rs` one line: `pub use generated::*;`.
//
// Everything else (including each `<pkg>/generated/mod.rs`) is owned
// by this emitter. A `git status` after `npm run specs:compile`
// surfaces drift; the snapshot test in tests/unit/emitter-rust.test.ts
// pins byte-stability for unchanged inputs.

import { mkdirSync, readFileSync, writeFileSync } from 'fs';
import { dirname, resolve as resolvePathFs } from 'path';
import type {
  EmitContext,
  Enum,
  Model,
  ModelProperty,
  Namespace,
  Operation,
  Program,
  Scalar,
  Type,
  Union,
} from '@typespec/compiler';
import { getDoc } from '@typespec/compiler';
import { listHttpOperationsIn } from '@typespec/http';
import {
  getActivityLabels,
  getActivityRouting,
  getMapsTo,
  getPreset,
} from 'typespec-workflow/decorators';

const BANNER = `// AUTO-GENERATED by codegen/emitters/typespec-emitter-rust. DO NOT EDIT.
// Re-emit with \`npm run specs:compile\` from the repo root.
`;

export async function $onEmit(context: EmitContext): Promise<void> {
  const program = context.program;
  const repoRoot = resolveRepoRoot(context);

  emitEnvPackage(program, repoRoot);
  emitTypesPackage(program, repoRoot);
  emitClientPackage(program, repoRoot);
  emitCorePackage(program, repoRoot);
  emitGovernPackage(program, repoRoot);
}

// ---------------------------------------------------------------------------
// Repo root and namespace helpers
// ---------------------------------------------------------------------------

function resolveRepoRoot(context: EmitContext): string {
  const main = context.program.projectRoot ?? process.cwd();
  let dir = main;
  for (let i = 0; i < 10; i++) {
    try {
      const pkg = JSON.parse(readFileSync(resolvePathFs(dir, 'package.json'), 'utf-8'));
      if (pkg.name === 'openbox-sdk') return dir;
    } catch {}
    const parent = dirname(dir);
    if (parent === dir) break;
    dir = parent;
  }
  throw new Error(`could not locate openbox-sdk repo root from ${main}`);
}

function findNamespace(program: Program, name: string): Namespace | undefined {
  return program.getGlobalNamespaceType().namespaces.get(name);
}

// Models / enums in interface declarations the runtime owns directly.
// Skipping them here means the emitter does not duplicate the contract
// the hand-written reqwest layer implements.
const INTERFACE_NAMES = new Set(['EnvLoader', 'TokenCodec', 'ClientNameResolver', 'OsPathResolver']);

// ---------------------------------------------------------------------------
// env package
// ---------------------------------------------------------------------------

function emitEnvPackage(program: Program, repoRoot: string): void {
  const ns = findNamespace(program, 'OpenboxEnv');
  if (!ns) throw new Error('OpenboxEnv namespace not found');

  const file: string[] = [BANNER, '// Source: specs/typespec/env/main.tsp.', ''];
  file.push('use serde::{Deserialize, Serialize};', '');

  const ctx: EmitNsContext = { program, file, emittedModels: collectEmittedModels(ns) };
  emitNamespaceTypes(ctx, ns);
  emitEnvironmentsConst(ctx, ns, repoRoot);

  writeGeneratedFile(repoRoot, 'rust/src/env/generated', 'env_bindings.rs', file);
  writeGeneratedModRs(repoRoot, 'rust/src/env/generated', ['env_bindings']);
  writeGeneratedReadme(repoRoot, 'rust/src/env/generated', [
    { source: 'specs/typespec/env/main.tsp', reproduces: 'env_bindings.rs: every enum and model declared in the OpenboxEnv namespace, plus the ENVIRONMENTS URL registry from specs/environments.json' },
  ]);
}

// ---------------------------------------------------------------------------
// types package: backend + core wire DTOs
// ---------------------------------------------------------------------------

function emitTypesPackage(program: Program, repoRoot: string): void {
  emitNamespaceFile(program, repoRoot, 'OpenboxBackend', 'rust/src/types/generated', 'backend.rs', 'specs/typespec/backend/main.tsp');
  emitNamespaceFile(program, repoRoot, 'OpenboxCore', 'rust/src/types/generated', 'core.rs', 'specs/typespec/core/main.tsp');
  writeGeneratedModRs(repoRoot, 'rust/src/types/generated', ['backend', 'core']);
  writeGeneratedReadme(repoRoot, 'rust/src/types/generated', [
    { source: 'specs/typespec/backend/main.tsp', reproduces: 'backend.rs: every model, enum, and union declared in the OpenboxBackend namespace' },
    { source: 'specs/typespec/core/main.tsp', reproduces: 'core.rs: every model, enum, and union declared in the OpenboxCore namespace' },
  ]);
}

