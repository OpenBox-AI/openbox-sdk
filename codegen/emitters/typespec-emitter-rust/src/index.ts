// ---------------------------------------------------------------------------
// govern package: canonical sets + spec-driven activity-label table
// ---------------------------------------------------------------------------
//
// Mirror of `emitGovernProtocol` in the TS emitter, narrowed to the
// pieces Rust consumers need today: the four canonical sets the TS side
// also exports plus the activity_type → display label table. The full
// protocol model graph (Workflow*, Verdict*, ActivityStage, etc.) is
// already reachable through `OpenboxCore` types; we don't re-emit it.
//
// Output lands at `rust/src/core/generated/govern.rs` to mirror the TS
// path `ts/src/core-client/generated/govern.ts`. The activity-label
// table is sorted by activity_type so callers can binary-search it.

function emitGovernPackage(program: Program, repoRoot: string): void {
  const ns = findNamespace(program, 'OpenboxGovern');
  const file: string[] = [BANNER, '// Source: specs/typespec/govern/main.tsp.', ''];

  if (!ns) {
    // Stub when the namespace is absent so the mod.rs reference
    // registered in `emitCorePackage` still compiles.
    file.push('// OpenboxGovern namespace not found in this spec compile.');
    file.push('');
    writeGeneratedFile(repoRoot, 'rust/src/core/generated', 'govern.rs', file);
    return;
  }

  // Canonical event-type strings live as the `CanonicalEventType` enum
  // on the namespace; pick the wire `value` string when present so we
  // never emit the TypeSpec member identifier by accident.
  const eventTypes: string[] = [];
  for (const [enumName, enumType] of ns.enums) {
    if (enumName !== 'CanonicalEventType') continue;
    for (const m of enumType.members.values()) {
      eventTypes.push(String(m.value ?? m.name));
    }
  }

  const verdictArms: string[] = [];
  for (const [enumName, enumType] of ns.enums) {
    if (enumName !== 'VerdictArm') continue;
    for (const m of enumType.members.values()) {
      verdictArms.push(String(m.value ?? m.name));
    }
  }

  // Activity-type vocabulary: union of every `@maps_to` activity_type
  // declared on a `@preset` interface plus every value in an
  // `@activityRouting` adapter table. Mirrors the TS emitter's walk so
  // the canonical set stays identical across language SDKs.
  const activityTypes = new Set<string>();
  for (const [, iface] of ns.interfaces) {
    if (!getPreset(program, iface)) continue;
    for (const [opName, op] of iface.operations) {
      const m = getMapsTo(program, op);
      if (m && m.activityType) {
        activityTypes.add(m.activityType);
      } else if (m) {
        activityTypes.add(opName);
      }
    }
  }
  const adaptersNs = findNamespace(program, 'OpenboxGovern.Adapters');
  if (adaptersNs) {
    for (const [, iface] of adaptersNs.interfaces) {
      for (const [, op] of iface.operations) {
        const ar = getActivityRouting(program, op);
        if (!ar) continue;
        for (const v of Object.values(ar.table)) activityTypes.add(v);
      }
    }
  }

  // `@activityLabels` decorator backs the curated display labels. Drop
  // entries that aren't in the canonical set so the emitted table never
  // drifts past the spec-recognized vocabulary; consumers fall back to
  // the title-case formatter in `openbox_sdk::approvals::format` for
  // anything not in this table.
  const labelsBinding = getActivityLabels(program, ns);
  const labelTable: Record<string, string> = {};
  if (labelsBinding) {
    for (const [k, v] of Object.entries(labelsBinding.table)) {
      if (activityTypes.has(k)) labelTable[k] = v;
    }
  }

  const sortedEventTypes = [...eventTypes].sort();
  const sortedActivityTypes = [...activityTypes].sort();
  const sortedVerdictArms = [...verdictArms].sort();
  const sortedLabelKeys = Object.keys(labelTable).sort();

  file.push('/// The canonical `event_type` strings the core service recognizes.');
  file.push('/// Anything else on the wire is a protocol bug.');
  file.push("pub const CANONICAL_EVENT_TYPES: &[&str] = &[");
  for (const t of sortedEventTypes) file.push(`    ${JSON.stringify(t)},`);
  file.push('];');
  file.push('');

  file.push('/// First-party `activity_type` vocabulary: every activity_type declared on a');
  file.push('/// `@preset` method or in an `@activityRouting` adapter table. Custom-preset');
  file.push('/// domain agents legitimately emit activity_types outside this set; treat it');
  file.push('/// as an allowlist for protocol-conformance reports, not for runtime gating.');
  file.push("pub const CANONICAL_ACTIVITY_TYPES: &[&str] = &[");
  for (const t of sortedActivityTypes) file.push(`    ${JSON.stringify(t)},`);
  file.push('];');
  file.push('');

  file.push('/// Spec-driven display labels for canonical activity_types. Sorted by');
  file.push('/// activity_type so `canonical_activity_label` can binary-search. Source of');
  file.push('/// truth for any UI that renders activity types (mobile, VS Code extension,');
  file.push('/// macOS approver, CLI list views). Consumers fall back to a title-case');
  file.push('/// formatter for activity_types not in this table; custom-preset domain');
  file.push("/// agents emit free-form strings that legitimately aren't covered here.");
  file.push("pub const CANONICAL_ACTIVITY_LABELS: &[(&str, &str)] = &[");
  for (const k of sortedLabelKeys) {
    file.push(`    (${JSON.stringify(k)}, ${JSON.stringify(labelTable[k])}),`);
  }
  file.push('];');
  file.push('');

  file.push('/// Look up the spec-driven display label for an `activity_type` string.');
  file.push('/// Returns `None` for non-canonical values; callers should fall back to a');
  file.push('/// title-case formatter (see `openbox_sdk::approvals::format::format_label`).');
  file.push('pub fn canonical_activity_label(activity_type: &str) -> Option<&\'static str> {');
  file.push('    CANONICAL_ACTIVITY_LABELS');
  file.push('        .binary_search_by(|(k, _)| (*k).cmp(activity_type))');
  file.push('        .ok()');
  file.push('        .map(|i| CANONICAL_ACTIVITY_LABELS[i].1)');
  file.push('}');
  file.push('');

  file.push('/// Every verdict arm the runtime emits. Production sets typically exclude');
  file.push('/// `constrain`; consumers can re-filter.');
  file.push("pub const CANONICAL_VERDICT_ARMS: &[&str] = &[");
  for (const a of sortedVerdictArms) file.push(`    ${JSON.stringify(a)},`);
  file.push('];');
  file.push('');

  writeGeneratedFile(repoRoot, 'rust/src/core/generated', 'govern.rs', file);
}

// ---------------------------------------------------------------------------
// Namespace → Rust file
// ---------------------------------------------------------------------------

interface EmitNsContext {
  program: Program;
  file: string[];
  /** Set of model names this namespace will emit. Used to decide
   *  whether a field-type reference resolves to an in-file struct or
   *  needs to fall back to `serde_json::Value`. Generic templates and
   *  their unnamed instantiations live outside this set. */
  emittedModels: Set<string>;
}

function emitNamespaceFile(
  program: Program,
  repoRoot: string,
  namespaceName: string,
  outDir: string,
  fileName: string,
  source: string,
): void {
  const ns = findNamespace(program, namespaceName);
  if (!ns) {
    // Empty file when the namespace is absent; mod.rs still wires it.
    const file = [BANNER, `// Source: ${source} (namespace ${namespaceName} not found).`, ''];
    writeGeneratedFile(repoRoot, outDir, fileName, file);
    return;
  }
  const file: string[] = [BANNER, `// Source: ${source}.`, ''];
  file.push('use serde::{Deserialize, Serialize};', '');
  const ctx: EmitNsContext = { program, file, emittedModels: collectEmittedModels(ns) };
  emitNamespaceTypes(ctx, ns);
  writeGeneratedFile(repoRoot, outDir, fileName, file);
}

function collectEmittedModels(ns: Namespace): Set<string> {
  const out = new Set<string>();
  for (const [name, m] of ns.models) {
    if (INTERFACE_NAMES.has(name)) continue;
    if (m.name === '') continue;
    if (isInstantiatedTemplate(m)) continue;
    out.add(m.name);
  }
  for (const [, e] of ns.enums) out.add(e.name);
  for (const [, u] of ns.unions) {
    if (u.name) out.add(u.name);
  }
  return out;
}

function emitNamespaceTypes(ctx: EmitNsContext, ns: Namespace): void {
  for (const [, e] of ns.enums) {
    appendEnum(ctx, e);
    ctx.file.push('');
  }
  for (const [, u] of ns.unions) {
    appendUnion(ctx, u);
    ctx.file.push('');
  }
  // Emit each declared model ONCE. Generic templates surface via their
  // uninstantiated form (`PaginatedResponse<T>`); we emit the generic
  // and skip every concrete instantiation that shares the same name.
  const seenNames = new Set<string>();
  for (const [name, m] of ns.models) {
    if (INTERFACE_NAMES.has(name)) continue;
    if (m.name === '') continue;
    // Drop concrete instantiations that resolve to the same name as
    // their template; the generic-emitted form covers them via type
    // arguments at the call site.
    if (isInstantiatedTemplate(m)) continue;
    if (seenNames.has(m.name)) continue;
    seenNames.add(m.name);
    appendModel(ctx, m);
    ctx.file.push('');
  }
}

function isInstantiatedTemplate(m: Model): boolean {
  return (m as { templateMapper?: unknown }).templateMapper !== undefined;
}

function isTemplateModel(m: Model): boolean {
  const node = (m as { node?: { templateParameters?: { length: number } } }).node;
  return (node?.templateParameters?.length ?? 0) > 0;
}

function templateArgs(m: Model): Type[] {
  const mapper = (m as { templateMapper?: { args?: Type[] } }).templateMapper;
  return mapper?.args ?? [];
}

function appendEnum(ctx: EmitNsContext, e: Enum): void {
  ctx.file.push(...renderDoc(getDoc(ctx.program, e)));
  ctx.file.push('#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]');
  ctx.file.push('#[serde(rename_all = "lowercase")]');
  ctx.file.push(`pub enum ${e.name} {`);
  for (const member of e.members.values()) {
    const wire = typeof member.value === 'string' ? member.value : member.name;
    if (wire !== member.name.toLowerCase()) {
      ctx.file.push(`    #[serde(rename = "${wire}")]`);
    }
    ctx.file.push(`    ${pascal(member.name)},`);
  }
  ctx.file.push('}');
}

function appendUnion(ctx: EmitNsContext, u: Union): void {
  ctx.file.push(...renderDoc(getDoc(ctx.program, u)));
  // Untagged unions of literals: emit as enum of unit variants.
  // Union of arbitrary types: bail to serde_json::Value alias.
  const variants = [...u.variants.values()];
  const allLiterals = variants.every((v) => v.type.kind === 'String');
  if (allLiterals) {
    ctx.file.push('#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]');
    ctx.file.push('#[serde(rename_all = "lowercase")]');
    ctx.file.push(`pub enum ${u.name ?? 'Anonymous'} {`);
    for (const v of variants) {
      const lit = (v.type as { value: string }).value;
      ctx.file.push(`    #[serde(rename = "${lit}")]`);
      ctx.file.push(`    ${pascal(String(v.name) || lit)},`);
    }
    ctx.file.push('}');
    return;
  }
  ctx.file.push(`pub type ${u.name ?? 'Anonymous'} = serde_json::Value;`);
}

function appendModel(ctx: EmitNsContext, m: Model): void {
  ctx.file.push(...renderDoc(getDoc(ctx.program, m)));
  ctx.file.push('#[derive(Debug, Clone, Serialize, Deserialize)]');
  const params = templateParamNames(m);
  if (params.length > 0) {
    // Let serde's derive infer per-field bounds (`T: Serialize`,
    // `T: Deserialize<'de>`) instead of declaring a where clause
    // ourselves. Mixing both produces E0283: the inferred lifetime
    // bound and a `DeserializeOwned` where clause look like two
    // distinct impls to the resolver.
    ctx.file.push(`pub struct ${m.name}<${params.join(', ')}> {`);
  } else {
    ctx.file.push(`pub struct ${m.name} {`);
  }
  for (const [propName, prop] of m.properties) {
    appendProperty(ctx, propName, prop, m);
  }
  ctx.file.push('}');
}

function templateParamNames(m: Model): string[] {
  // TypeSpec's template AST node varies in shape between compiler
  // versions and the surface API does not expose a typed accessor.
  // The `node` carries either:
  //   - `node.templateParameters: TemplateParameterDeclarationNode[]`
  //   - `node.templateParameters: { parameters: ...[] }` (older shape)
  // Try both. Returns [] for instantiated forms.
  if ((m as { templateMapper?: unknown }).templateMapper) return [];
  const node = (m as {
    node?: {
      templateParameters?:
        | { parameters?: { id: { sv: string } }[] }
        | { id: { sv: string } }[];
    };
  }).node;
  const tp = node?.templateParameters;
  if (!tp) return [];
  if (Array.isArray(tp)) {
    return tp.map((p) => p.id.sv);
  }
  return (tp.parameters ?? []).map((p) => p.id.sv);
}

function appendProperty(
  ctx: EmitNsContext,
  propName: string,
  prop: ModelProperty,
  parent: Model,
): void {
  for (const line of renderDoc(getDoc(ctx.program, prop))) ctx.file.push(`    ${line}`);
  const rustName = snake(propName);
  const ty = renderType(ctx, prop.type, prop.optional, parent);
  if (rustName !== propName) {
    ctx.file.push(`    #[serde(rename = "${propName}")]`);
  }
  if (prop.optional) {
    ctx.file.push('    #[serde(default, skip_serializing_if = "Option::is_none")]');
  }
  ctx.file.push(`    pub ${escapeRustKeyword(rustName)}: ${ty},`);
}

function renderType(ctx: EmitNsContext, t: Type, optional: boolean, parent?: Model): string {
  const inner = renderTypeInner(ctx, t, parent);
  // Self-reference (e.g. `cause: ErrorInfo` on `model ErrorInfo`) is
  // infinite-size in Rust unless boxed. Detect and wrap before
  // applying `Option<>` so it stays `Option<Box<Self>>`.
  if (parent && t.kind === 'Model' && (t as Model).name === parent.name) {
    return optional ? `Option<Box<${inner}>>` : `Box<${inner}>`;
  }
  return optional ? `Option<${inner}>` : inner;
}

function renderTypeInner(ctx: EmitNsContext, t: Type, parent?: Model): string {
  switch (t.kind) {
    case 'Scalar':
      return renderScalar(t as Scalar);
    case 'Model': {
      const m = t as Model;
      // `T[]` inside a template body is the `Array` model with
      // `templateMapper.args = [T]` but no resolved `indexer` yet.
      // Cover both the resolved and unresolved cases.
      if (m.name === 'Array') {
        if (m.indexer) {
          return `Vec<${renderTypeInner(ctx, m.indexer.value, parent)}>`;
        }
        const arg = templateArgs(m)[0];
        if (arg) return `Vec<${renderTypeInner(ctx, arg, parent)}>`;
      }
      if (m.name === 'Record') {
        if (m.indexer) {
          return `std::collections::HashMap<String, ${renderTypeInner(ctx, m.indexer.value, parent)}>`;
        }
        const arg = templateArgs(m)[0];
        if (arg) return `std::collections::HashMap<String, ${renderTypeInner(ctx, arg, parent)}>`;
      }
      if (parent && m.name === parent.name) return m.name;
      // Instantiated template: render with concrete type arguments,
      // e.g. `PaginatedResponse<Agent>`.
      if (isInstantiatedTemplate(m)) {
        const args = templateArgs(m).map((a) => renderTypeInner(ctx, a));
        if (m.name && ctx.emittedModels.has(m.name)) {
          return args.length > 0 ? `${m.name}<${args.join(', ')}>` : m.name;
        }
        return 'serde_json::Value';
      }
      if (m.name && ctx.emittedModels.has(m.name)) return m.name;
      return 'serde_json::Value';
    }
    case 'Enum':
      return (t as Enum).name;
    case 'TemplateParameter':
      return (t as { node: { id: { sv: string } } }).node.id.sv;
    case 'Union': {
      const u = t as Union;
      if (u.name && ctx.emittedModels.has(u.name)) return u.name;
      const variants = [...u.variants.values()];
      if (variants.every((v) => v.type.kind === 'String')) {
        // Fallback: untagged literal union the emitter did not name.
        // Carry as a String; downstream callers compare against
        // literals.
        return 'String';
      }
      return 'serde_json::Value';
    }
    case 'Intrinsic':
      return 'serde_json::Value';
    default:
      // Unknown kind. Surface in stderr during emit so we can iterate
      // on coverage; runtime safety stays via serde_json::Value.
      // eslint-disable-next-line no-console
      console.error(`[typespec-emitter-rust] unhandled type kind: ${t.kind}`);
      return 'serde_json::Value';
  }
}

function renderScalar(s: Scalar): string {
  switch (s.name) {
    case 'string':
      return 'String';
    case 'boolean':
      return 'bool';
    case 'int8':
      return 'i8';
    case 'int16':
      return 'i16';
    case 'int32':
      return 'i32';
    case 'int64':
    case 'safeint':
    case 'integer':
      return 'i64';
    case 'uint8':
      return 'u8';
    case 'uint16':
      return 'u16';
    case 'uint32':
      return 'u32';
    case 'uint64':
      return 'u64';
    case 'float':
    case 'float32':
      return 'f32';
    case 'float64':
    case 'numeric':
      return 'f64';
    case 'decimal':
    case 'decimal128':
      return 'String';
    case 'bytes':
      return 'Vec<u8>';
    case 'url':
      return 'String';
    case 'utcDateTime':
    case 'offsetDateTime':
    case 'plainDate':
    case 'plainTime':
    case 'duration':
      return 'String';
    default:
      // Custom scalar: surface as its own type so a separate `pub type`
      // alias can be hand-written if needed.
      return s.name ? pascal(s.name) : 'String';
  }
}

