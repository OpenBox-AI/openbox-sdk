function requireModel(ns: Namespace, name: string): Model {
  const model = ns.models.get(name);
  if (!model) throw new Error(`Missing TypeSpec model ${ns.name}.${name}`);
  return model;
}

function enumValues(ns: Namespace, name: string): string[] {
  const enumType = ns.enums.get(name);
  if (!enumType) throw new Error(`Missing TypeSpec enum ${ns.name}.${name}`);
  return [...enumType.members.values()].map((member) => String(member.value ?? member.name));
}

function modelPropertyNames(model: Model): string[] {
  return [...model.properties.keys()];
}

function propertyStringLiterals(model: Model, propertyName: string): string[] {
  const property = model.properties.get(propertyName);
  if (!property) throw new Error(`Missing TypeSpec property ${model.name}.${propertyName}`);
  return stringLiteralsFromType(property.type);
}

function stringLiteralsFromType(type: Type): string[] {
  switch (type.kind) {
    case 'String':
      return [String(type.value)];
    case 'Enum':
      return [...type.members.values()].map((member) => String(member.value ?? member.name));
    case 'Union':
      return [...type.variants.values()].flatMap((variant) =>
        stringLiteralsFromType(variant.type),
      );
    default:
      return [];
  }
}

function validatedAliasMap(
  entries: Array<Record<string, unknown> & { target: string }>,
  fieldGroups: Array<{ name: string; fields: string[] }>,
  label: string,
): Record<string, string[]> {
  const byGroup = Object.fromEntries(fieldGroups.map((group) => [group.name, group.fields]));
  const result: Record<string, string[]> = {};
  for (const entry of entries) {
    const aliases: string[] = [];
    for (const groupName of Object.keys(byGroup)) {
      const values = asArray(entry[groupName]);
      assertSubset(values, byGroup[groupName], `${label}.${entry.target} ${groupName}`);
      aliases.push(...values);
    }
    aliases.push(...asArray(entry.extra));
    result[entry.target] = uniqueStrings(aliases);
  }
  return result;
}

function assertSubset(values: string[], allowed: string[], label: string): void {
  const missing = values.filter((value) => !allowed.includes(value));
  if (missing.length > 0) {
    throw new Error(`${label} references fields not present in TypeSpec: ${missing.join(', ')}`);
  }
}

function requireValue(values: string[], expected: string, label: string): string {
  if (!values.includes(expected)) {
    throw new Error(`${label} is missing expected value ${expected}`);
  }
  return expected;
}

function requireValues(values: string[], expected: string[], label: string): string[] {
  for (const value of expected) {
    requireValue(values, value, label);
  }
  return expected;
}

function asArray(value: unknown): string[] {
  if (value === undefined || value === null) return [];
  return Array.isArray(value) ? value.map(String) : [String(value)];
}

function arrayOfStrings(value: unknown): string[] {
  return Array.isArray(value)
    ? value.filter((entry): entry is string => typeof entry === 'string')
    : [];
}

function arrayOfRecords(value: unknown): Record<string, unknown>[] {
  return Array.isArray(value)
    ? value.filter(
        (entry): entry is Record<string, unknown> =>
          entry !== null && typeof entry === 'object' && !Array.isArray(entry),
      )
    : [];
}

function uniqueStrings(values: string[]): string[] {
  return [...new Set(values.filter((value) => value !== undefined && value !== null))];
}

function snakeCase(name: string): string {
  const value = name
    .replace(/([a-z0-9])([A-Z])/g, '$1_$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1_$2')
    .replace(/[^A-Za-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')
    .toLowerCase();
  if (!value) return 'operation';
  return PYTHON_KEYWORDS.has(value) ? `${value}_` : value;
}

function pythonPascalCase(name: string): string {
  return name
    .split(/[^A-Za-z0-9]+/)
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join('');
}

function regexForPath(pathPattern: string): string {
  const escaped = pathPattern.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  return `^${escaped.replace(/\\\{[^/]+\\\}/g, '[^/]+')}$`;
}

function eventTypeConstantName(value: string): string {
  return `${value
    .replace(/([a-z0-9])([A-Z])/g, '$1_$2')
    .replace(/[^A-Za-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')
    .toUpperCase()}_EVENT_TYPE`;
}

function snakeToCamel(value: string): string {
  return value.replace(/_([a-z0-9])/g, (_, char: string) => char.toUpperCase());
}

function py(value: unknown, indent = 0): string {
  const pad = ' '.repeat(indent);
  const childPad = ' '.repeat(indent + 2);
  if (value === null || value === undefined) return 'None';
  if (typeof value === 'boolean') return value ? 'True' : 'False';
  if (typeof value === 'number') return Number.isFinite(value) ? String(value) : 'None';
  if (typeof value === 'string') return JSON.stringify(value);
  if (Array.isArray(value)) {
    if (value.length === 0) return '[]';
    return `[\n${value.map((item) => `${childPad}${py(item, indent + 2)}`).join(',\n')}\n${pad}]`;
  }
  if (typeof value === 'object') {
    const entries = Object.entries(value as Record<string, unknown>)
      .filter(([, entry]) => entry !== undefined);
    if (entries.length === 0) return '{}';
    return `{\n${entries
      .map(([key, entry]) => `${childPad}${JSON.stringify(key)}: ${py(entry, indent + 2)}`)
      .join(',\n')}\n${pad}}`;
  }
  return 'None';
}

function findNamespace(program: Program, name: string): Namespace | undefined {
  if (name.includes('.')) {
    const parts = name.split('.');
    let cur: Namespace | undefined = program.getGlobalNamespaceType();
    for (const part of parts) {
      if (!cur) return undefined;
      cur = cur.namespaces.get(part);
    }
    return cur;
  }
  const stack = [program.getGlobalNamespaceType()];
  while (stack.length) {
    const ns = stack.pop()!;
    if (ns.name === name) return ns;
    for (const sub of ns.namespaces.values()) stack.push(sub);
  }
  return undefined;
}
