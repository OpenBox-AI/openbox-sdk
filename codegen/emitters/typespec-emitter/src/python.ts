function collectGovernManifest(program: Program): PythonPresetEntry[] {
  const ns = findNamespace(program, 'OpenboxGovern');
  if (!ns) return [];
  const presets: PythonPresetEntry[] = [];
  for (const [, iface] of ns.interfaces) {
    const preset = getPreset(program, iface);
    if (!preset) continue;
    const isCustom = preset.name === 'custom';
    const methods: PythonPresetMethod[] = [];
    for (const [opName, op] of iface.operations) {
      if (isCustom) {
        methods.push({ name: opName });
        continue;
      }
      const mapping = getMapsTo(program, op);
      if (!mapping) continue;
      methods.push({
        name: opName,
        eventType: mapping.eventType,
        activityType: mapping.activityType ?? opName,
      });
    }
    if (methods.length > 0) {
      presets.push({ preset: preset.name, methods });
    }
  }
  return presets.sort((a, b) => a.preset.localeCompare(b.preset));
}

function emitGovern(governManifest: PythonPresetEntry[]): string {
  const classes = [];
  const registry = [];
  const presetActivityTypes: Record<string, Record<string, string>> = {};
  for (const preset of governManifest) {
    const className = `${pythonPascalCase(preset.preset)}Session`;
    const presetKey = snakeCase(preset.preset);
    registry.push([presetKey, className]);
    presetActivityTypes[preset.preset] = Object.fromEntries(
      preset.methods.flatMap((method) =>
        method.activityType ? [[method.name, method.activityType]] : [],
      ),
    );
    if (presetKey === 'custom') {
      classes.push([
        `class ${className}(BaseGovernedSession):`,
        `    async def activity(self, activity_type: str, stage: ActivityStage, payload: GovernedPayload | None = None) -> WorkflowVerdict:`,
        `        event_type = "ActivityStarted" if stage == "pre" else "ActivityCompleted"`,
        `        return await self.run_activity(event_type, activity_type, payload or {})`,
        '',
      ].join('\n'));
      continue;
    }
    const methods = preset.methods
      .map((method) => {
        const methodName = snakeCase(method.name);
        return [
          `    async def ${methodName}(self, payload: GovernedPayload | None = None) -> WorkflowVerdict:`,
          `        return await self.run_activity(${JSON.stringify(method.eventType)}, ${JSON.stringify(method.activityType)}, payload or {})`,
          '',
        ].join('\n');
      })
      .join('\n');
    classes.push(`class ${className}(BaseGovernedSession):\n${methods || '    pass\n'}`);
  }
  const registryLiteral = `{\n${registry.map(([key, cls]) => `    ${JSON.stringify(key)}: ${cls},`).join('\n')}\n}`;
  return `${PYTHON_BANNER}from types import SimpleNamespace

from openbox_sdk._govern_runtime import ActivityStage, BaseGovernedSession, GovernedPayload, WorkflowVerdict

PRESET_MANIFEST = ${py(governManifest)}
PRESET_ACTIVITY_TYPES = ${py(presetActivityTypes)}


${classes.join('\n\n')}

PRESET_CLASSES = ${registryLiteral}
presets = SimpleNamespace(**PRESET_CLASSES)

__all__ = ["PRESET_MANIFEST", "PRESET_ACTIVITY_TYPES", "PRESET_CLASSES", "presets", ${registry.map(([, cls]) => JSON.stringify(cls)).join(', ')}]
`;
}

