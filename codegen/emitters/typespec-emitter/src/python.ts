export function emitPythonSdk(program: Program, repoRoot: string): void {
  const outDir = resolvePath(repoRoot, 'python/openbox_sdk/generated');
  const backendNamespace = findNamespace(program, 'OpenboxBackend');
  const methodNames = backendNamespace
    ? getSdkMethodNames(program, backendNamespace) ?? {}
    : {};
  const methodPermissions = backendNamespace
    ? getBackendPermissions(program, backendNamespace) ?? {}
    : {};

  const backendManifest = collectEndpointManifest(program, 'OpenboxBackend', methodNames);
  const coreManifest = collectEndpointManifest(program, 'OpenboxCore', methodNames);
  const governManifest = collectGovernManifest(program);

  rmSync(outDir, { recursive: true, force: true });
  writePythonFile(resolvePath(outDir, '__init__.py'), `${PYTHON_BANNER}__all__: list[str] = []\n`);
  writePythonFile(
    resolvePath(outDir, 'backend_client.py'),
    emitClientModule({
      className: 'BackendOperationsMixin',
      manifestName: 'BACKEND_ENDPOINT_MANIFEST',
      manifest: backendManifest,
    }),
  );
  writePythonFile(
    resolvePath(outDir, 'core_client.py'),
    emitClientModule({
      className: 'CoreOperationsMixin',
      manifestName: 'CORE_ENDPOINT_MANIFEST',
      manifest: coreManifest,
    }),
  );
  writePythonFile(
    resolvePath(outDir, 'permissions.py'),
    emitPermissions(backendManifest, methodPermissions),
  );
  writePythonFile(resolvePath(outDir, 'schemas.py'), emitSchemas(program));
  writePythonFile(resolvePath(outDir, 'capability_matrix.py'), emitCapabilityMatrix(program));
  writePythonFile(resolvePath(outDir, 'sdk_targets.py'), emitSdkTargets(program));
  writePythonFile(resolvePath(outDir, 'govern.py'), emitGovern(governManifest));
  writePythonFile(resolvePath(outDir, 'runtime_contract.py'), emitRuntimeContract(program));
}

function writePythonFile(file: string, content: string): void {
  mkdirSync(dirname(file), { recursive: true });
  writeFileSync(file, `${content.trimEnd()}\n`);
}

function collectEndpointManifest(
  program: Program,
  namespaceName: string,
  methodNames: Record<string, string>,
): PythonEndpointEntry[] {
  const ns = findNamespace(program, namespaceName);
  if (!ns) return [];
  const [ops] = listHttpOperationsIn(program, ns);
  return ops.map((op) => ({
    operation_id: op.operation.name,
    method_name: methodNameFor(op.operation.name, methodNames),
    verb: op.verb.toLowerCase(),
    path: op.path,
    path_pattern: op.path.replace(/\{[^}]+\}/g, '{x}'),
  }));
}

function methodNameFor(operationId: string, methodNames: Record<string, string>): string {
  const mapped = methodNames[operationId];
  if (mapped) return snakeCase(mapped);
  const suffix = operationId.includes('_')
    ? operationId.split('_').slice(1).join('_')
    : operationId;
  return snakeCase(suffix);
}

function emitClientModule(opts: {
  className: string;
  manifestName: string;
  manifest: PythonEndpointEntry[];
}): string {
  const methods = opts.manifest
    .map((entry) =>
      [
        `    async def ${entry.method_name}(self, *, path_params: JsonMapping | None = None, query: JsonMapping | None = None, data: Any = None) -> Any:`,
        `        path = render_path(${JSON.stringify(entry.path)}, path_params)`,
        `        return await self._request(${JSON.stringify(entry.verb)}, path, params=query, data=data, operation=${JSON.stringify(entry.operation_id)})`,
        '',
      ].join('\n'),
    )
    .join('\n');
  return `${PYTHON_BANNER}from typing import Any, Protocol, TypeAlias

from openbox_sdk._utils import render_path

JsonMapping: TypeAlias = dict[str, Any]

${opts.manifestName} = ${py(opts.manifest)}
OPERATION_TO_ENDPOINT = {entry["operation_id"]: entry for entry in ${opts.manifestName}}
OPERATION_METHOD_NAMES = {entry["operation_id"]: entry["method_name"] for entry in ${opts.manifestName}}
METHOD_OPERATION_NAMES = {entry["method_name"]: entry["operation_id"] for entry in ${opts.manifestName}}


class _Requester(Protocol):
    async def _request(self, method: str, path: str, *, params: JsonMapping | None = None, data: Any = None, operation: str | None = None) -> Any: ...


class ${opts.className}:
    def _resolve_operation(self, operation_id: str, path_params: JsonMapping | None = None) -> tuple[str, str]:
        try:
            entry = OPERATION_TO_ENDPOINT[operation_id]
        except KeyError as exc:
            raise ValueError(f"unknown OpenBox operation_id: {operation_id}") from exc
        return str(entry["verb"]), render_path(str(entry["path"]), path_params)

${methods}`;
}

function emitPermissions(
  manifest: PythonEndpointEntry[],
  permissions: Record<string, string[]>,
): string {
  const rules = [];
  for (const item of manifest) {
    const required = permissions[item.operation_id];
    if (!required || required.length === 0) continue;
    rules.push([
      item.verb,
      regexForPath(item.path_pattern),
      item.method_name,
      required,
    ]);
  }
  return `${PYTHON_BANNER}PATH_PERMISSION_RULES = ${py(rules)}\n`;
}

function emitSchemas(program: Program): string {
  const names = new Set<string>();
  for (const namespaceName of ['OpenboxBackend', 'OpenboxCore']) {
    const ns = findNamespace(program, namespaceName);
    if (ns) collectModelClassNames(ns, names);
  }
  const classes = [...names]
    .map((name) => pythonPascalCase(name))
    .filter(Boolean)
    .sort();
  const uniqueClasses = [...new Set(classes)];
  const classBodies = uniqueClasses
    .map((name) => `class ${name}(OpenBoxGeneratedModel):\n    pass\n`)
    .join('\n\n');
  return `${PYTHON_BANNER}from pydantic import BaseModel, ConfigDict


class OpenBoxGeneratedModel(BaseModel):
    model_config = ConfigDict(extra="allow", populate_by_name=True)


${classBodies}
__all__ = ${py(['OpenBoxGeneratedModel', ...uniqueClasses])}
`;
}

function collectModelClassNames(ns: Namespace, out: Set<string>): void {
  for (const [name] of ns.models) {
    if (name === 'Array' || name === 'Record') continue;
    if (name) out.add(name);
  }
  for (const child of ns.namespaces.values()) {
    collectModelClassNames(child, out);
  }
}

