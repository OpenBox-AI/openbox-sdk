function emitClientModule({ className, manifestName, manifest, methodNames }) {
  const entries = manifest.map((item) => {
    const methodName = methodNameFor(item.operationId, methodNames);
    return {
      operation_id: item.operationId,
      method_name: methodName,
      verb: item.verb,
      path: item.path,
      path_pattern: item.pathPattern,
    };
  });
  const methods = entries
    .map((entry) => {
      return [
        `    async def ${entry.method_name}(self, *, path_params: JsonMapping | None = None, query: JsonMapping | None = None, data: Any = None) -> Any:`,
        `        path = render_path(${JSON.stringify(entry.path)}, path_params)`,
        `        return await self._request(${JSON.stringify(entry.verb)}, path, params=query, data=data, operation=${JSON.stringify(entry.operation_id)})`,
        "",
      ].join("\n");
    })
    .join("\n");
  return `${banner}from typing import Any, Protocol, TypeAlias\n\nfrom openbox_sdk._utils import render_path\n\nJsonMapping: TypeAlias = dict[str, Any]\n\n${manifestName} = ${py(entries)}\nOPERATION_TO_ENDPOINT = {entry["operation_id"]: entry for entry in ${manifestName}}\nOPERATION_METHOD_NAMES = {entry["operation_id"]: entry["method_name"] for entry in ${manifestName}}\nMETHOD_OPERATION_NAMES = {entry["method_name"]: entry["operation_id"] for entry in ${manifestName}}\n\n\nclass _Requester(Protocol):\n    async def _request(self, method: str, path: str, *, params: JsonMapping | None = None, data: Any = None, operation: str | None = None) -> Any: ...\n\n\nclass ${className}:\n    def _resolve_operation(self, operation_id: str, path_params: JsonMapping | None = None) -> tuple[str, str]:\n        try:\n            entry = OPERATION_TO_ENDPOINT[operation_id]\n        except KeyError as exc:\n            raise ValueError(f\"unknown OpenBox operation_id: {operation_id}\") from exc\n        return str(entry[\"verb\"]), render_path(str(entry[\"path\"]), path_params)\n\n${methods}`;
}

function emitPermissions(manifest, methodNames, permissions) {
  const rules = [];
  for (const item of manifest) {
    const required = permissions[item.operationId];
    if (!required || required.length === 0) continue;
    rules.push([
      item.verb,
      regexForPath(item.pathPattern),
      methodNameFor(item.operationId, methodNames),
      required,
    ]);
  }
  return `${banner}PATH_PERMISSION_RULES = ${py(rules)}\n`;
}

function emitSchemas() {
  const backend = readJson(backendOpenApiPath);
  const core = readJson(coreOpenApiPath);
  const schemas = {
    ...((backend.components && backend.components.schemas) || {}),
    ...((core.components && core.components.schemas) || {}),
  };
  const classNames = Object.keys(schemas)
    .map((name) => pascalCase(name))
    .filter(Boolean)
    .sort();
  const unique = [...new Set(classNames)];
  const classes = unique
    .map((name) => `class ${name}(OpenBoxGeneratedModel):\n    pass\n`)
    .join("\n\n");
  return `${banner}from pydantic import BaseModel, ConfigDict\n\n\nclass OpenBoxGeneratedModel(BaseModel):\n    model_config = ConfigDict(extra=\"allow\", populate_by_name=True)\n\n\n${classes}\n__all__ = ${py(["OpenBoxGeneratedModel", ...unique])}\n`;
}

