function readJson(file) {
  return JSON.parse(fs.readFileSync(file, "utf8"));
}

function readText(file) {
  return fs.readFileSync(file, "utf8");
}

function extractArray(file, constName) {
  const value = extractConstJson(file, constName);
  if (!Array.isArray(value)) throw new Error(`${constName} in ${file} is not an array`);
  return value;
}

function extractConstJson(file, constName) {
  const source = fs.readFileSync(file, "utf8");
  const marker = `const ${constName}`;
  const start = source.indexOf(marker);
  if (start < 0) throw new Error(`Could not find ${constName} in ${file}`);
  const equals = source.indexOf("=", start);
  if (equals < 0) throw new Error(`Could not find ${constName} assignment in ${file}`);
  const open = source.slice(equals + 1).search(/[\[{]/);
  if (open < 0) throw new Error(`Could not find ${constName} JSON literal in ${file}`);
  const literalStart = equals + 1 + open;
  let depth = 0;
  let inString = false;
  let escaped = false;
  for (let i = literalStart; i < source.length; i += 1) {
    const char = source[i];
    if (inString) {
      if (escaped) {
        escaped = false;
      } else if (char === "\\") {
        escaped = true;
      } else if (char === '"') {
        inString = false;
      }
      continue;
    }
    if (char === '"') inString = true;
    if (char === "[" || char === "{") depth += 1;
    if (char === "]" || char === "}") {
      depth -= 1;
      if (depth === 0) return JSON.parse(source.slice(literalStart, i + 1));
    }
  }
  throw new Error(`Unclosed ${constName} JSON literal in ${file}`);
}

function extractBlock(source, keyword, name) {
  const marker = `${keyword} ${name}`;
  const start = source.indexOf(marker);
  if (start < 0) throw new Error(`Could not find ${keyword} ${name}`);
  const open = source.indexOf("{", start);
  if (open < 0) throw new Error(`Could not find ${keyword} ${name} block`);
  let depth = 0;
  for (let i = open; i < source.length; i += 1) {
    const char = source[i];
    if (char === "{") depth += 1;
    if (char === "}") {
      depth -= 1;
      if (depth === 0) return source.slice(open + 1, i);
    }
  }
  throw new Error(`Unclosed ${keyword} ${name} block`);
}

function extractEnumValues(file, enumName) {
  const body = extractBlock(readText(file), "enum", enumName);
  return body
    .split("\n")
    .map((line) => line.replace(/\/\/.*$/, "").trim())
    .filter(Boolean)
    .flatMap((line) => {
      const match = line.match(/^([A-Za-z_][A-Za-z0-9_]*)(?:\s*:\s*"([^"]+)")?\s*,?/);
      if (!match) return [];
      return [match[2] || match[1]];
    });
}

function extractModelProperties(file, modelName) {
  const body = extractBlock(readText(file), "model", modelName);
  const properties = [];
  for (const match of body.matchAll(
    /^\s*(?:@[A-Za-z_][A-Za-z0-9_]*(?:\([^)]*\))?\s*)*([A-Za-z_][A-Za-z0-9_]*)\??\s*:/gm,
  )) {
    properties.push(match[1]);
  }
  return properties;
}

function extractPropertyStringLiterals(file, modelName, propertyName) {
  const body = extractBlock(readText(file), "model", modelName);
  const match = body.match(
    new RegExp(`\\b${propertyName}\\??\\s*:\\s*([^;]+);`),
  );
  if (!match) throw new Error(`Could not find ${modelName}.${propertyName}`);
  return [...match[1].matchAll(/"([^"]+)"/g)].map((entry) => entry[1]);
}

function snakeCase(name) {
  const value = name
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1_$2")
    .replace(/[^A-Za-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .toLowerCase();
  if (!value) return "operation";
  return PYTHON_KEYWORDS.has(value) ? `${value}_` : value;
}

function pascalCase(name) {
  return name
    .split(/[^A-Za-z0-9]+/)
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join("");
}

function methodNameFor(operationId, methodNames) {
  const mapped = methodNames[operationId];
  if (mapped) return snakeCase(mapped);
  const suffix = operationId.includes("_") ? operationId.split("_").slice(1).join("_") : operationId;
  return snakeCase(suffix);
}

function regexForPath(pathPattern) {
  const escaped = pathPattern.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  return `^${escaped.replace(/\\\{[^/]+\\\}/g, "[^/]+")}$`;
}

function py(value) {
  return JSON.stringify(value, null, 2)
    .replace(/\btrue\b/g, "True")
    .replace(/\bfalse\b/g, "False")
    .replace(/\bnull\b/g, "None");
}

function snakeToCamel(value) {
  return value.replace(/_([a-z0-9])/g, (_, char) => char.toUpperCase());
}

function eventTypeConstantName(value) {
  return `${value
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/[^A-Za-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .toUpperCase()}_EVENT_TYPE`;
}

function assertSubset(values, allowed, label) {
  const missing = values.filter((value) => !allowed.includes(value));
  if (missing.length > 0) {
    throw new Error(`${label} references fields not present in TypeSpec: ${missing.join(", ")}`);
  }
}

function requireValue(values, expected, label) {
  if (!values.includes(expected)) {
    throw new Error(`${label} is missing expected value ${expected}`);
  }
  return expected;
}

function requireValues(values, expected, label) {
  for (const value of expected) {
    requireValue(values, value, label);
  }
  return expected;
}

function asArray(value) {
  if (value === undefined || value === null) return [];
  return Array.isArray(value) ? value : [value];
}

function unique(values) {
  return [...new Set(values.filter((value) => value !== undefined && value !== null))];
}

function validatedAliasMap(entries, fieldGroups, label) {
  const byGroup = Object.fromEntries(fieldGroups.map((group) => [group.name, group.fields]));
  const result = {};
  for (const entry of entries) {
    const aliases = [];
    for (const groupName of Object.keys(byGroup)) {
      const values = asArray(entry[groupName]);
      assertSubset(values, byGroup[groupName], `${label}.${entry.target} ${groupName}`);
      aliases.push(...values);
    }
    aliases.push(...asArray(entry.extra));
    result[entry.target] = unique(aliases);
  }
  return result;
}

