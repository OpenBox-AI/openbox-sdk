#!/usr/bin/env node
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const repoRoot = process.cwd();
const fixturePath = resolve(repoRoot, 'codegen/fixtures/provider-capabilities.json');
const markdownPath = resolve(repoRoot, 'docs/governance-artifacts/capability-checklist.md');
const checklistCsvPath = resolve(repoRoot, 'docs/governance-artifacts/capability-checklist.csv');
const csvPath = resolve(repoRoot, 'docs/governance-artifacts/summary.csv');

const fixture = JSON.parse(readFileSync(fixturePath, 'utf8'));
const score = arrayField(fixture, 'governanceChecklistScore');
const limitations = arrayField(fixture, 'governanceChecklistLimitations');
const rows = arrayField(fixture, 'governanceChecklistRows');

writeFile(markdownPath, renderMarkdown());
writeFile(checklistCsvPath, renderChecklistCsv());
writeFile(csvPath, renderCsv());

console.log(`wrote ${relativePath(markdownPath)}`);
console.log(`wrote ${relativePath(checklistCsvPath)}`);
console.log(`wrote ${relativePath(csvPath)}`);

function arrayField(value, key) {
  const field = value?.[key];
  if (!Array.isArray(field)) {
    throw new Error(`${fixturePath} is missing array field ${key}`);
  }
  return field;
}

function writeFile(path, text) {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, text.endsWith('\n') ? text : `${text}\n`);
}

function renderMarkdown() {
  const lines = [
    '# OpenBox Governance Capability Checklist',
    '',
    '<!--',
    'AUTO-GENERATED by scripts/write-governance-checklist.mjs.',
    'Source: codegen/fixtures/provider-capabilities.json.',
    'Do not edit manually.',
    '-->',
    '',
    '## Source',
    '',
    '- Contract: `specs/typespec/govern/capabilities.tsp`',
    '- Fixture: `codegen/fixtures/provider-capabilities.json`',
    '- Generate: `npm run governance:checklist`',
    '',
    '## Claim Boundary',
    '',
    '- `100%` means scored OpenBox-owned checklist coverage.',
    '- `[~]` rows are host-owned or caller-owned limitations and stay visible.',
    '- Missing rows must remain `0`.',
    '- Detailed rows are emitted from `governanceChecklistRows`.',
    '',
    '## Summary',
    '',
  ];

  for (const row of score) {
    lines.push(`### ${row.area}`, '');
    lines.push(...summaryLines(row), '');
  }

  lines.push('## Limitations', '');

  for (const limitation of limitations) {
    lines.push(`### ${limitation.checklistId}`, '');
    lines.push(`- Boundary owner: ${limitation.boundaryOwner}`);
    lines.push(...wrapListItem('Limitation', limitation.limitation), '');
  }

  lines.push('## Detailed Checklist', '');
  for (const area of checklistAreas()) {
    lines.push(...renderAreaRows(area));
  }

  lines.push(
    '## CSV',
    '',
    '- Summary: `docs/governance-artifacts/summary.csv`',
    '- Detailed rows: `docs/governance-artifacts/capability-checklist.csv`',
  );
  return lines.join('\n');
}

function summaryLines(row) {
  if (row.scope === 'universal') {
    return ['- Total: ' + row.total, '- Scored coverage: n/a'];
  }
  return [
    `- Total: ${row.total}`,
    `- Done: ${row.done}`,
    `- Limitations: ${row.limitations}`,
    `- Missing: ${row.missing}`,
    `- Scored total: ${row.scoredTotal}`,
    `- Scored done: ${row.scoredDone}`,
    `- Scored done percent: ${row.scoredDonePercent}`,
  ];
}

function checklistAreas() {
  return score
    .map((row) => row.area)
    .filter((area) => area !== 'Implementation Total' && rows.some((row) => row.area === area));
}

function renderAreaRows(area) {
  const areaScore = score.find((entry) => entry.area === area);
  const areaRows = rows.filter((row) => row.area === area);
  const lines = [`### ${area} Rows`, ''];
  if (areaScore) {
    lines.push(...summaryLines(areaScore), '');
  }

  let currentGroup = undefined;
  for (const row of areaRows) {
    if (row.group && row.group !== currentGroup) {
      currentGroup = row.group;
      if (lines.at(-1) !== '') lines.push('');
      lines.push(`#### ${row.group}`, '');
    }
    lines.push(...renderChecklistRow(row));
  }
  lines.push('');
  return lines;
}

function renderChecklistRow(row) {
  const marker = statusMarker(row.status);
  const requirement = row.status === 'limitation'
    ? `Limitation: ${row.requirement}`
    : row.requirement;
  return wrapText(`${marker}${requirement}`, `${row.id} - `, '  ');
}

function statusMarker(status) {
  if (status === 'done') return '[x] ';
  if (status === 'limitation') return '[~] ';
  if (status === 'missing') return '[ ] ';
  return '';
}

function renderCsv() {
  const header = [
    'Area',
    'Total',
    'Done',
    'Limitations',
    'Missing',
    'Scored Total',
    'Scored Done',
    'Scored Done %',
  ];
  const rows = score.map((row) => [
    row.area,
    row.total,
    csvMetric(row, 'done'),
    csvMetric(row, 'limitations'),
    csvMetric(row, 'missing'),
    csvMetric(row, 'scoredTotal'),
    csvMetric(row, 'scoredDone'),
    row.scoredDonePercent,
  ]);
  return [header, ...rows].map((row) => row.map(csvCell).join(',')).join('\n');
}

function renderChecklistCsv() {
  const header = [
    'Area',
    'Group',
    'ID',
    'Status',
    'Scored',
    'Provider',
    'Boundary Owner',
    'Requirement',
  ];
  const body = rows.map((row) => [
    row.area,
    row.group,
    row.id,
    row.status,
    row.scored,
    row.provider ?? '',
    row.boundaryOwner ?? '',
    row.requirement,
  ]);
  return [header, ...body].map((row) => row.map(csvCell).join(',')).join('\n');
}

function csvMetric(row, key) {
  return row.scope === 'universal' ? 'n/a' : row[key];
}

function csvCell(value) {
  const text = String(value);
  return /[",\n]/.test(text) ? `"${text.replaceAll('"', '""')}"` : text;
}

function wrapListItem(label, text) {
  return wrapText(text, `- ${label}: `, '  ');
}

function wrapText(text, firstPrefix, nextPrefix, width = 80) {
  const words = text.split(/\s+/).filter(Boolean);
  const lines = [];
  let prefix = firstPrefix;
  let line = prefix;

  for (const word of words) {
    const separator = line === prefix ? '' : ' ';
    if (line.length + separator.length + word.length > width && line !== prefix) {
      lines.push(line);
      prefix = nextPrefix;
      line = `${prefix}${word}`;
      continue;
    }
    line += `${separator}${word}`;
  }

  lines.push(line);
  return lines;
}

function relativePath(path) {
  return path.startsWith(`${repoRoot}/`) ? path.slice(repoRoot.length + 1) : path;
}
